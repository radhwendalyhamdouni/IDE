export async function POST(request: NextRequest) {
  try {
    const body: AIRequest = await request.json();
    const { prompt, context, files, action = 'generate' } = body;

    // استخدام z-ai-web-dev-sdk للتواصل مع AI
    const zai = await ZAI.create();
    
    // بناء الرسائل
    const messages: AIMessage[] = [
      { role: 'system', content: VIBE_SYSTEM_PROMPT },
    ];
    
    // إضافة السياق إذا وجد
    if (context) {
      messages.push({ 
        role: 'user', 
        content: `السياق الحالي:\n\`\`\`\n${context}\n\`\`\`\n\nالطلب: ${prompt}` 
      });
    } else if (files && Object.keys(files).length > 0) {
      const filesContext = Object.entries(files)
        .map(([name, content]) => `// ${name}\n${content}`)
        .join('\n\n');
      messages.push({ 
        role: 'user', 
        content: `الملفات الحالية:\n\`\`\`\n${filesContext}\n\`\`\`\n\nالطلب: ${prompt}` 
      });
    } else {
      messages.push({ role: 'user', content: prompt });
    }

    // استدعاء AI
    const completion = await zai.chat.completions.create({
      messages,
      temperature: 0.7,
      max_tokens: 2000,
    });

    const generatedCode = completion.choices[0]?.message?.content || generateCodeFromPatterns(prompt);

    return NextResponse.json({
      success: true,
      code: generatedCode,
      explanation: action === 'explain' ? generatedCode : undefined,
      action,
      model: 'z-ai-model',
    });

  } catch (error) {
    console.error('AI Error:', error);
    
    // Fallback للأنماط المحلية
    try {
      const body = await request.json();
      const fallbackCode = generateCodeFromPatterns(body.prompt);
      
      return NextResponse.json({
        success: true,
        code: fallbackCode,
        explanation: 'تم التوليد باستخدام الأنماط المحلية',
        action: body.action || 'generate',
        model: 'pattern-matching',
      });
    } catch {
      return NextResponse.json(
        { success: false, error: 'فشل في معالجة الطلب' },
        { status: 500 }
      );
    }
  }
}

