// دالة لإنشاء كود من النص العربي باستخدام الأنماط
function generateCodeFromPatterns(prompt: string): string {
  const lowerPrompt = prompt.toLowerCase();
  
  // أنماط المواقع
  if (lowerPrompt.includes('موقع') || lowerPrompt.includes('صفحة') || lowerPrompt.includes('تطبيق') || lowerPrompt.includes('أنشئ')) {
    return generateWebApp(prompt);
  }
  
  // أنماط الواجهات
  if (lowerPrompt.includes('واجهة') || lowerPrompt.includes('ui') || lowerPrompt.includes('زر') || lowerPrompt.includes('نموذج')) {
    return generateUI(prompt);
  }
  
  // أنماط المتغيرات
  if (lowerPrompt.includes('متغير') || lowerPrompt.includes('أنشئ متغير')) {
    const varMatch = prompt.match(/متغير\s+(\w+)\s+(?:يساوي|=)\s+(.+)/);
    if (varMatch) {
      return `متغير ${varMatch[1]} = ${varMatch[2]};`;
    }
    return `متغير قيمة = 0; // تم إنشاء متغير`;
  }
  
  // أنماط الطباعة
  if (lowerPrompt.includes('اطبع') || lowerPrompt.includes('اعرض')) {
    const printMatch = prompt.match(/(?:اطبع|اعرض)\s*["']?(.+?)["']?/);
    if (printMatch) {
      return `اطبع("${printMatch[1]}");`;
    }
    return `اطبع("مرحباً بالعالم!");`;
  }
  
  // أنماط الدوال
  if (lowerPrompt.includes('دالة') || lowerPrompt.includes('function')) {
    const funcMatch = prompt.match(/دالة\s+(\w+)\s*\(([^)]*)\)/);
    if (funcMatch) {
      return `دالة ${funcMatch[1]}(${funcMatch[2]}) {\n    // TODO: أضف المنطق هنا\n}`;
    }
    return `دالة دالة_جديدة() {\n    اطبع("مرحباً!");\n}`;
  }
  
  // النمط الافتراضي - إنشاء تطبيق بسيط
  return generateWebApp(prompt);
}

