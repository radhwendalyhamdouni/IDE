// دالة توليد الكود من النص العربي
// ─────────────────────────────────────────────────────────────────────────────
export function generateCodeFromPatterns(prompt: string): string {
  const normalizedPrompt = prompt.trim();
  
  // ترتيب الأنماط حسب الأولوية
  const sortedPatterns = [...PATTERNS].sort((a, b) => b.priority - a.priority);
  
  // البحث عن أول نمط مطابق
  for (const pattern of sortedPatterns) {
    for (const regex of pattern.patterns) {
      const match = normalizedPrompt.match(regex);
      if (match) {
        console.log(`[Pattern Matched] ${pattern.description}`);
        return pattern.generator(match, normalizedPrompt);
      }
    }
  }
  
  // إذا لم يتم العثور على نمط، إنشاء كود افتراضي
  return generateDefaultCode(normalizedPrompt);
}

// ─────────────────────────────────────────────────────────────────────────────
// توليد كود افتراضي
// ─────────────────────────────────────────────────────────────────────────────
function generateDefaultCode(prompt: string): string {
  // محاولة استخراج معلومات مفيدة من النص
  const words = prompt.split(/\s+/);
  
  // البحث عن أرقام
  const numbers = words.filter(w => /^\d+$/.test(w));
  
  // البحث عن كلمات مفتاحية
  const hasVariable = words.some(w => ['متغير', 'ثابت', 'عرّف', 'أنشئ'].includes(w));
  const hasPrint = words.some(w => ['اطبع', 'اعرض', 'اكتب'].includes(w));
  const hasFunction = words.some(w => ['دالة', 'function'].includes(w));
  const hasCondition = words.some(w => ['إذا', 'لو', 'شرط'].includes(w));
  const hasLoop = words.some(w => ['كرر', 'طالما', 'لكل', 'حلقة'].includes(w));
  
  // توليع كود بناءً على الكلمات المفتاحية المكتشفة
  if (hasPrint) {
    const text = prompt.replace(/اطبع|اعرض|اكتب/g, '').trim();
    return `اطبع("${text || 'مرحباً بالعالم'}")؛`;
  }
  
  if (hasVariable && numbers.length > 0) {
    const varName = words[words.indexOf('متغير') + 1] || words[words.indexOf('أنشئ') + 2] || 'قيمة';
    return `متغير ${varName} = ${numbers[0]}؛`;
  }
  
  if (hasFunction) {
    return `دالة دالة_جديدة() {\n    // أضف المنطق هنا\n    اطبع("مرحباً!")؛\n}`;
  }
  
  if (hasCondition) {
    return `إذا شرط {\n    اطبع("تحقق الشرط")؛\n} وإلا {\n    اطبع("لم يتحقق الشرط")؛\n}`;
  }
  
  if (hasLoop) {
    return `لكل عنصر في القائمة {\n    اطبع(عنصر)؛\n}`;
  }
  
  // كود افتراضي
  return `// لم يتم التعرف على النمط بشكل كامل
// النص: "${prompt}"
// جرب كتابة:
// - "أنشئ متغير اسم يساوي أحمد"
// - "اطبع مرحبا بالعالم"
// - "أنشئ دالة تجمع رقمين"
// - "أنشئ تطبيق مهام"

اطبع("مرحباً بك في لغة المرجع!")؛`;
}

// ─────────────────────────────────────────────────────────────────────────────
// دوال مساعدة للتحليل
// ─────────────────────────────────────────────────────────────────────────────
export function analyzePrompt(prompt: string): {
  category: string;
  keywords: string[];
  complexity: 'سهل' | 'متوسط' | 'متقدم';
} {
  const lowerPrompt = prompt.toLowerCase();
  
  // تحديد الفئة
  let category = 'عام';
  if (lowerPrompt.includes('متغير') || lowerPrompt.includes('ثابت')) category = 'متغيرات';
  else if (lowerPrompt.includes('اطبع') || lowerPrompt.includes('اعرض')) category = 'طباعة';
  else if (lowerPrompt.includes('إذا') || lowerPrompt.includes('شرط')) category = 'شروط';
  else if (lowerPrompt.includes('دالة')) category = 'دوال';
  else if (lowerPrompt.includes('كرر') || lowerPrompt.includes('طالما') || lowerPrompt.includes('لكل')) category = 'حلقات';
  else if (lowerPrompt.includes('تطبيق') || lowerPrompt.includes('موقع') || lowerPrompt.includes('متجر')) category = 'تطبيقات';
  else if (lowerPrompt.includes('شبكة') || lowerPrompt.includes('عصبية') || lowerPrompt.includes('ذكاء')) category = 'AI/ML';
  
  // استخراج الكلمات المفتاحية
  const keywords: string[] = [];
  Object.values(KEYWORDS).forEach(kwList => {
    kwList.forEach(kw => {
      if (lowerPrompt.includes(kw.toLowerCase())) {
        keywords.push(kw);
      }
    });
  });
  
  // تحديد التعقيد
  let complexity: 'سهل' | 'متوسط' | 'متقدم' = 'سهل';
  if (keywords.length > 3 || lowerPrompt.includes('تطبيق') || lowerPrompt.includes('نظام')) {
    complexity = 'متقدم';
  } else if (keywords.length > 1 || lowerPrompt.includes('دالة') || lowerPrompt.includes('شرط')) {
    complexity = 'متوسط';
  }
  
  return { category, keywords, complexity };
}
